void	ft_rev_int_tab(int *tab, int size)
{
	int t;
	int i;
	int u;

	t = 0;
	i = 0;
	u = size - 1;
	while (i < (size / 2))
	{
		t = *(tab + i);
		*(tab + i) = *(tab + u);
		*(tab + u) = t;
		i++;
		u--;
	}
}
